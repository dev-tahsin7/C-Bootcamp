#include <stdio.h>

// While Loop 
int main(){
    int x = 100; 
    
    while (x >= 0){
        printf("Sorry\n"); 
        x-- ; 
    }
}

// Do while Loop 

int main(){
      int x = 100; 
    
    do{
        printf("Sorry\n"); 
        x-- ; 
    }
    while(x >= 0); 
    
}

// For Loop

int main(){
    
    int x ; 
    for(x = 1; x <= 100; x++){
        printf("I Love You\n"); 
    }
}


