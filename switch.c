#include <stdio.h>


int main(){
    // switch
    
    int x = 4;
    
    switch (x){
        case 1:
        printf("Saturday");
        break ; 
        
        case 2: 
        printf("Sunday");
        break; 
        
        case 3: 
        printf("Monday"); 
        break; 
        
        default:
            printf("Nothing"); 
        
    }
}